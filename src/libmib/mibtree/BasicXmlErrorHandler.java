package libmib.mibtree;

import org.xml.sax.ErrorHandler;
import org.xml.sax.SAXException;
import org.xml.sax.SAXParseException;

/**
 * This class reports all types of XML parsing errors by re-throwing the
 * passed exception.  It is useful for halting parsing if any kind of 
 * problem occurs, even if it is supposedly recoverable.
 * 
 */
public class BasicXmlErrorHandler implements ErrorHandler 
{

    /**
     * 
     */
    public BasicXmlErrorHandler() 
    {
        super();
    }

    /* (non-Javadoc)
     * @see org.xml.sax.ErrorHandler#warning(org.xml.sax.SAXParseException)
     */
    public void warning(SAXParseException e) throws SAXException 
    {
        throw e;
    }

    /* (non-Javadoc)
     * @see org.xml.sax.ErrorHandler#error(org.xml.sax.SAXParseException)
     */
    public void error(SAXParseException e) throws SAXException 
    {
        throw e;
    }

    /* (non-Javadoc)
     * @see org.xml.sax.ErrorHandler#fatalError(org.xml.sax.SAXParseException)
     */
    public void fatalError(SAXParseException e) throws SAXException 
    {
        throw e;
    }

}
