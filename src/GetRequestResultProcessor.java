/**
 * Abstract class used to define how the intermediary retrieved results from a 
 * GetRequestTask's execution are handled.
 */
public abstract class GetRequestResultProcessor
{
	/**
	 * Handles the intermediary results of a GetRequestTask's execution.
	 * @param result the GetRequestResult to handle
	 */
	public abstract void processResult(GetRequestResult result);
}
